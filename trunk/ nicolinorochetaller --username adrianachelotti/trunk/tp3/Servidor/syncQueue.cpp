///////////////////////////////////////////////////////////////////////
char* syncQueue::pop(){

	char* ret;

	ret = this->q.front();

	this->q.pop_front();

	return ret;
}

//////////////////////////////////////////////////////////////////////

int syncQueue::items(){

	return this->q.size();

}


///////////////////////////////////////////////////////////////////////

void syncQueue::push(char* x){

	this->q.push_back(x);

	return;

}



